class car:
    def __init__(self, name):
        self.name = name
        self.factory = None
        self.engine = None

    @classmethod
    def create_car(cls, name,  factory, engine):
        car = cls(name)
        car.factory = factory
        car.engine = engine
        return car
    
class factory:
    def __init__(self, name):
        self.name = name

class engine:
    def __init__(self, name):
        self.name = name

